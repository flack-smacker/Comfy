
package com.muro.compilers.comfy.grammar

/**
 * The Terminal class contains named constants corresponding to each terminal
 * that appears in our grammar.
 */
object Terminal {
  val OpenBrace = "{"
  val CloseBrace = "}"
  val OpenParen = "("
  val CloseParen = ")"
  val DoubleQuote = "\""
  val NotOperator = "!"
  val AssignmentOp = "="
  val EndOfProgram = "$"
  val If = "if"
  val Print = "print"
  val While = "while"
  val Int = "int"
  val String = "string"
  val Boolean = "boolean"
  val EqualsOp = "=="
  val NotEqualsOp = "!="  
  val BoolTrue = "true"
  val BoolFalse = "false"
  val Plus = "+"
  val Space = " "
  val Newline = "\n"
  val Char = "Char"
  val Id = "Id"
  val Operator = "Operator"
  val Digit = "Digit"
}
